router.get("/login/:id",function(req,res){
  if (req.session.user) {
    users = req.session.user;
    res.render("index",{name:users.name,password:users.password});
  }
  models.user.find(req.params.id).success(function(users){
    req.session.user = users;
    res.render("index",{name:users.name,password:users.password});
  });
});

router.get("/notformark",function(req,res){
  if (req.session.user){
    if (req.session.user.name == "mark2"){
      res.render("ok");
    }
  }
  res.redirect("/");
});

router.get("/logout",function(req,res){
  req.session = null;
  res.redirect("/");
});

